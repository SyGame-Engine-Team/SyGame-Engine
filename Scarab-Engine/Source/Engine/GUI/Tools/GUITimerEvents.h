/////////////////////////////////////////////////////////////////////////////////
// Constants definitions

// Limits
#define GUITIMEREVENTS_MAX 256

// Prototypes
class GUIEventProcessor;

// Timer
typedef struct _guitimer {
	GUIEventProcessor * pTarget;
	Double fIntervalMS, fLastTickMS;
} GUITimer;

/////////////////////////////////////////////////////////////////////////////////
// The GUITimerEvents class
class GUITimerEvents
{
public:
    GUITimerEvents();
    ~GUITimerEvents();

    UInt CreateTimer( GUIEventProcessor * pTarget, Double fIntervalMS );
    Void DestroyTimer( UInt iTimerID );

    inline UInt GetTimerCount() const;
    inline const GUITimer * GetTimer( UInt iTimerID ) const;
    inline GUITimer * GetTimer( UInt iTimerID );

private:
    UInt m_iTimerCount;
    GUITimer m_arrTimers[GUITIMEREVENTS_MAX];
};

