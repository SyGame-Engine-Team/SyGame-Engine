/////////////////////////////////////////////////////////////////////////////////
// Includes
#include "../../GUIWidget.h"

/////////////////////////////////////////////////////////////////////////////////
// Constants definitions
#define GUILABEL_MAX_LENGTH 256

/////////////////////////////////////////////////////////////////////////////////
// The GUILabelModel class
class GUILabelModel : public GUIWidgetModel
{
public:
    GUILabelModel( const GChar * strText );
    virtual ~GUILabelModel();

    // Model
    inline const GChar * GetText( UInt * outLength = NULL ) const;
    Void SetText( const GChar * strText );

    // Controller
    virtual Void OnTextChange();

protected:
    UInt m_iLabelLength;
    GChar m_strText[GUILABEL_MAX_LENGTH];
};

/////////////////////////////////////////////////////////////////////////////////
// The GUILabel class
class GUILabel : public GUIWidget
{
public:
    GUILabel( GUILabelModel * pModel, GUIWidget * pParent, const GUILayout & guiLayout, const GChar * strName );
    virtual ~GUILabel();

    // Type
	inline virtual GUIWidgetType GetType() const;

protected:
    // Drawing interface
    virtual Void _Draw( const Rectangle2 & rectClient );
};

