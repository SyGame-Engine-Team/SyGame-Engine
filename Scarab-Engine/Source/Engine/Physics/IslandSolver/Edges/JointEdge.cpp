// JointEdge implementation
JointEdge::JointEdge( BodyNode * pNodeA, BodyNode * pNodeB, JointConstraint * pJoint ):
    IslandEdge( pNodeA, pNodeB )
{
    Assert( pJoint != NULL );
    m_pJointConstraint = pJoint;
}
JointEdge::~JointEdge()
{
    PhysicsFn->SelectMemory( TEXT("Joints") );
    Delete( m_pJointConstraint );
    PhysicsFn->UnSelectMemory();
}
