/////////////////////////////////////////////////////////////////////////////////
// Constants definitions

/////////////////////////////////////////////////////////////////////////////////
// The Resource interface
class Resource
{
public:
    Resource();
    virtual ~Resource();

    // Typename support
    static const GChar * GetResourceTypeName( ResourceType iType );
    static ResourceType GetResourceType( const GChar * strResourceTypeName );

    // Type access
    virtual ResourceType GetResourceType() const = 0;

    // Database access
    Bool Load( const GChar * strDBName, const GChar * strResPath );
    Bool Save( const GChar * strDBName, const GChar * strResPath ) const;

protected:
    virtual Bool _OnLoad( ResourceDB * pRDB, RDB_ResourceHandle * hResource ) = 0;
    virtual Bool _OnSave( ResourceDB * pRDB, RDB_ResourceHandle * hResource ) const = 0;
};

