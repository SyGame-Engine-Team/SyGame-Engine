#include "WorldSwitchNode.h"

/////////////////////////////////////////////////////////////////////////////////
// WorldSwitchNode implementation
WorldSwitchNode::WorldSwitchNode( const GChar * strName ):
    WorldNode( strName )
{
    m_strActiveChild = NULL;
}
WorldSwitchNode::~WorldSwitchNode()
{
    // nothing to do
}

/////////////////////////////////////////////////////////////////////////////////

Void WorldSwitchNode::_OnVisibleGroup( WorldCuller & wCuller, Bool bNoCull )
{
    if ( m_strActiveChild == NULL )
        return;
    WorldBaseNode * pChild = GetChild( m_strActiveChild );
    pChild->_QueryVisibleGroup( wCuller, bNoCull );
}

