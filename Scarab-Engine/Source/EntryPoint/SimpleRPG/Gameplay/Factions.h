/////////////////////////////////////////////////////////////////////////////////
// Constants definitions

// Factions list
enum FactionID {
    FACTIONID_UNDEFINED = 0,

    // Player factions
    FACTIONID_PLAYER_RED,
    FACTIONID_PLAYER_GREEN,
    FACTIONID_PLAYER_BLUE,
    FACTIONID_PLAYER_YELLOW,

    // Monster factions
    FACTIONID_MONSTER_HOSTILE, // Allways hostile

    FACTIONID_COUNT
};

/////////////////////////////////////////////////////////////////////////////////
// The FactionRegistry class
class FactionRegistry
{
public:
    FactionRegistry();
    virtual ~FactionRegistry();

    // Getters
    inline FactionID GetParentFaction() const;
    inline Void SetParentFaction( FactionID idParentFaction );

    // Hostility methods
    Bool IsHostile( FactionID idFaction ) const;

protected:
    // Hostility map
    static Bool sm_mapHostility[FACTIONID_COUNT * FACTIONID_COUNT];

    // Parent faction
    FactionID m_idParentFaction;
};

