///////////////////////////////////////////////////////////////////////////////
// The Datastruct class
class Datastruct
{
public:
    Datastruct();
    virtual ~Datastruct();

    // Fixed memory context
    inline virtual Void UseMemoryContext( MemoryContextID idMemoryContext, const GChar * strAllocatorName );

    // Common minimal methods
    virtual Void Create() = 0;
    virtual Void Destroy() = 0;

    virtual UInt MemorySize() const = 0;
    virtual UInt Count() const = 0;
    inline Bool IsEmpty() const;

    virtual Void Clear() = 0;

protected:
    // Memory interface
    inline Void _SelectMemory();
    inline Void _UnSelectMemory();

private:
    // Fixed memory context
    MemoryContextID m_idMemoryContext;
    const GChar * m_strAllocatorName;
};

