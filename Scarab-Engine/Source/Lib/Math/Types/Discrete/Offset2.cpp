// Offset2 implementation
const Offset2 Offset2::Null = Offset2(0, 0);
const Offset2 Offset2::eI = Offset2(1, 0);
const Offset2 Offset2::eJ = Offset2(0, 1);

Offset2::Offset2()
{
    // nothing to do
}
Offset2::Offset2(Int x, Int y)
{
    X = x;
    Y = y;
}
Offset2::Offset2(const Int vArr[2])
{
    X = vArr[0];
    Y = vArr[1];
}
Offset2::Offset2(const Offset2 & rhs)
{
    X = rhs.X;
    Y = rhs.Y;
}
Offset2::~Offset2()
{
    // nothing to do
}

