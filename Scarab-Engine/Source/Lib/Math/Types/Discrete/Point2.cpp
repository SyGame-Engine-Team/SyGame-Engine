// Point2 implementation
const Point2 Point2::Null = Point2(0, 0);

Point2::Point2()
{
    // nothing to do
}
Point2::Point2(Int x, Int y)
{
    X = x;
    Y = y;
}
Point2::Point2(const Int vArr[2])
{
    X = vArr[0];
    Y = vArr[1];
}
Point2::Point2(const Point2 & rhs)
{
    X = rhs.X;
    Y = rhs.Y;
}
Point2::~Point2()
{
    // nothing to do
}

