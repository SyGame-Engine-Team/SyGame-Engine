// Point3 implementation
const Point3 Point3::Null = Point3(0, 0, 0);

Point3::Point3()
{
    // nothing to do
}
Point3::Point3(Int x, Int y, Int z)
{
    X = x;
    Y = y;
    Z = z;
}
Point3::Point3(const Int vArr[3])
{
    X = vArr[0];
    Y = vArr[1];
    Z = vArr[2];
}
Point3::Point3(const Point3 & rhs)
{
    X = rhs.X;
    Y = rhs.Y;
    Z = rhs.Z;
}
Point3::~Point3()
{
    // nothing to do
}

